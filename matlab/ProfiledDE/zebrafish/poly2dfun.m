r(2) = p(1) + p(2)*y(1) + p(3)*y(2) + p(4)*y(1)*y(2) +...
    p(5)*y(1)^2 + p(6)*y(2)^2 + p(7)*y(1)^2*y(2) + p(9)*y(1)*y(2)^2 + ...
    p(10)*y(1)^3 + p(11)*y(2)^3 + p(12)*y(1)^3*y(2) + p(13)*y(1)*y(2)^3 + ...
    p(14)*y(1)^2*y(2)^2 + p(15)*y(1)^4 + p(16)*y(2)^4 + p(17)*y(1)^3*y(2)*2 + ...
    p(18)*y(1)^2*y(1)^3 + p(19)*y(1)^3*y(2)^3;
