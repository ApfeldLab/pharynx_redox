function r = lincubode(t,y,p)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% fhnfun
%
% The FitzHugh-Nagumo equations in vector form
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

r = y;
r(1) = p(1)*y(1)+p(2)*y(2) + p(5)*y(1).^3;
r(2) = p(3)*y(1)+p(4)*y(2);


end