function r = rossfunode(t,y,p)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% rossfunode
%
% The Rossler equations in scalar form
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

r = y;
r(1) = -y(2) - y(3);
r(2) = y(1) + p(1)*y(2);
r(3) = p(2) + (y(1)-p(3))*y(3);

end